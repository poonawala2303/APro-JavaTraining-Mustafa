package com.aurionpro;

import java.util.Scanner;

public class Information {
	public static void main(String[] args) {
		Scanner inputVariable = new Scanner(System.in);
		
		System.out.println("Enter your full name: ");
		String name = inputVariable.nextLine();
		
		System.out.println("Enter your email address: ");
		String emailAddress = inputVariable.nextLine();
		
		System.out.println("Enter your resedential address: ");
		String address = inputVariable.nextLine();
		
		System.out.println("Enter your age: ");
		int age = inputVariable.nextInt();
		
		System.out.println("Enter your DOB: ");
		String dateOfBirth = inputVariable.nextLine();
		
		System.out.println("Enter your company name: ");
		String companyName = inputVariable.nextLine();
		
		System.out.println("=========================================");
		
		System.out.println("~~~~~~~~~~Personal Information~~~~~~~~~~");
		System.out.println("Name: " + name);
		System.out.println("Email Address: " + emailAddress);
		System.out.println("Resedential Address: " + address);
		System.out.println("Age: " + age);
		System.out.println("Date of Birth: " + dateOfBirth);
		System.out.println("Company Name: " + companyName);
		
	}
}
