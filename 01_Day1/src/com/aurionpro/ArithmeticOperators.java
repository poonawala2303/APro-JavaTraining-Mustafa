package com.aurionpro;

public class ArithmeticOperators {
	public static void main(String[] args) {

		int number1 = 30;
		int number2 = 12;

		int sum = number1 + number2;
		int product = number1 * number2;
		int difference = number1 - number2;
		int division = number1 / number2;

		System.out.println("The sum of " + number1 + " and " + number2 + " is "
				+ sum);
		System.out.println("The product of " + number1 + " and " + number2
				+ " is " + product);
		System.out.println("The difference of " + number1 + " and " + number2
				+ " is " + difference);
		System.out.println("The division of " + number1 + " and " + number2
				+ " is " + division);

	}
}
